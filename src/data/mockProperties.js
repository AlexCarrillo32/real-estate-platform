// Helper function to filter properties
export function filterProperties(properties, filters) {
  return properties.filter(property => {
    if (filters.priceMin && property.price < filters.priceMin) return false
    if (filters.priceMax && property.price > filters.priceMax) return false
    if (filters.bedrooms && property.bedrooms < filters.bedrooms) return false
    if (filters.bathrooms && property.bathrooms < filters.bathrooms)
      return false
    if (filters.propertyType && property.propertyType !== filters.propertyType)
      return false
    if (filters.location) {
      const searchTerm = filters.location.toLowerCase()
      const searchableText =
        `${property.address} ${property.city} ${property.state} ${property.zipCode}`.toLowerCase()
      if (!searchableText.includes(searchTerm)) return false
    }
    if (filters.sqftMin && property.sqft < filters.sqftMin) return false
    if (filters.sqftMax && property.sqft > filters.sqftMax) return false

    return true
  })
}

// Helper function to sort properties
export function sortProperties(properties, sortBy = 'newest') {
  const sorted = [...properties]

  switch (sortBy) {
    case 'price-asc':
      return sorted.sort((a, b) => a.price - b.price)
    case 'price-desc':
      return sorted.sort((a, b) => b.price - a.price)
    case 'newest':
      return sorted.sort(
        (a, b) => new Date(b.listingDate) - new Date(a.listingDate)
      )
    case 'oldest':
      return sorted.sort(
        (a, b) => new Date(a.listingDate) - new Date(b.listingDate)
      )
    case 'beds-desc':
      return sorted.sort((a, b) => b.bedrooms - a.bedrooms)
    case 'sqft-desc':
      return sorted.sort((a, b) => b.sqft - a.sqft)
    default:
      return sorted
  }
}
